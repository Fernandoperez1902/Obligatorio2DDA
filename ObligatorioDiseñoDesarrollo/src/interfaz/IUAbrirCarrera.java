    public void cargarCarrera(){
        Jornada jornada = seleccionado.buscarJornada(new Date(DateFormat.MEDIUM));
        carrera = jornada.traerProximaCarrera();
    }
    
    public void mostrarProximaCarrera(){
        
        
        lblNumero.setText(Integer.toString(carrera.getNumeroCarrera()));
        lblNombre.setText(carrera.getNombre());
    }

    public ArrayList<String> convertirLista(ArrayList<Participante> aConvertir){
        
        ArrayList<String> lista = new ArrayList<String>();
        for (Participante p : aConvertir){
            lista.add("NÂ°" + p.getNumero() + " - " + p.getNombreCaballo() + " - $"+ p.getDividendo());
        }
        
        return lista;
    }
    
    
    public void cargarParticipantes(ArrayList<Participante> participantes){
        
        ArrayList<String> stringParticipante = convertirLista(participantes);
        lstParticipantes.setListData(stringParticipante.toArray());
        
        
    }
    

